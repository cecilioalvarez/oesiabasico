package com.arquitecturajava.cecilio.inicio.complementario;

public class PrincipalSwitch {

	public static void main(String[] args) {

		int categoria = 1;

		switch (categoria) {

		case 1:
			System.out.println("categoria 1");
			break;
		case 2:
			System.out.println("categoria 2");
			break;
		case 3:
			System.out.println("categoria 3");
			break;

		default:
			System.out.println("no hacemos nada");
		}

	}

}
